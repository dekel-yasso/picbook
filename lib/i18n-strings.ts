export type StringKey = keyof (typeof STRINGS)['en'];

export function translate(lang: Lang, key: StringKey, params?: Record<string, string | number>): string {
  let s: string = STRINGS[lang][key] ?? STRINGS.en[key] ?? key;
  if (params) for (const [k, v] of Object.entries(params)) s = s.replaceAll(`{${k}}`, String(v));
  return s;
}

export function themeLabel(lang: Lang, theme: string): string {
  return THEME_LABELS[lang]?.[theme] ?? theme;
}
